    public static int countCharacters(String[] words, String chars) {
        int res = 0;

        int[] charCount = new int[26];
        for (int i = 0; i < chars.length(); i++) {
            charCount[chars.charAt(i) - 'a']++;
        }

        for (String tempStr : words) {
            int[] charCountCopy = Arrays.copyOf(charCount, charCount.length);
            int count = 0;

            for (char tempChar : tempStr.toCharArray()) {
                charCountCopy[tempChar - 'a']--;

                if (charCountCopy[tempChar - 'a'] < 0) {
                    count = 0;
                    break;
                }

                count++;
            }

            res += count;
        }

        return res;
    }

